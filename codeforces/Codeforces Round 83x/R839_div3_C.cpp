void solve() {
    int k, n;
    cin >> k >> n;
    int last = 1;
    cout << last << " ";
    for (int i = 1; i < k; i++) {
        int rest = k - i - 1;
        if (last + i + rest <= n) {
            last += i;
        } else last++;
        cout << last << " ";
    }
    cout << endl;
}

