void solve() {
    int n;
    cin >> n;
    for (int i = 0; 7 * i <= n; i++) {
        for (int j = 0; 5 * j <= n; j++) {
            int t = n - i * 7 - j * 5;
            if (t % 3 == 0) {
                cout << t / 3 << " " << j << " " << i << endl;
                return;
            }
        }
    }
    cout << -1 << endl;
}

