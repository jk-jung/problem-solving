void solve() {
    int n, k;
    cin >> n >> k;
    int r = 1 << 30;
    for (int i = 1; i <= n; i++) {
        if (n % i == 0) {
            int j = n / i;
            if (j < k) r = min(r, i * k + j);
            if (i < k) r = min(r, j * k + i);
        }
    }
    cout << r << endl;
}

