void solve() {
    int n;
    cin >> n;
    vi v(n);
    for (int &x: v)cin >> x;
    sort(v.begin(), v.end());

    int c = 0, pos = 0, r = 0;
    for (int i = 0; i <= n; i++) {
        while (pos < i)i = max(i, v[pos++] + 1), c++;
        while (pos < n && v[pos] <= c)pos++, c++;
        if (i == c){
            r++;
        }
    }
    cout << r << endl;
}

