void solve() {
    int n;
    cin >> n;
    vi v(n);
    for (int &x: v)cin >> x;

    for (int i = 0; i < n; i++) {
        int s = 0, e = i, r = -1;
        while (s <= e) {
            int m = (s + e) / 2;
            int len = (i - m + 1);
            if (v[m] >= len)r = m, e = m - 1;
            else s = m + 1;
        }
        cout << (i - r + 1) << " ";
    }
    cout << endl;
}

