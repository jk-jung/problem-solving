void solve() {
    int n;
    cin >> n;
    map<int, int> ck;
    for(int i=0;i<n;i++){
        int c;
        cin >> c;
        ck[c]++;
    }
    vi v(n + 1);
    int r = 0;
    for (auto [x, y]: ck) {
        for (int i = x; i <= n; i += x) {
            v[i] += y;
            r = max(r, v[i]);
        }
    }
    cout << r << endl;
}

