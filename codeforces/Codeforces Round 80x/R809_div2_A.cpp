void solve() {
    int n, m;
    cin >> n >> m;
    vi v(n);
    for (int &x: v)cin >> x;
    string r = string(m, 'B');

    for (int x: v) {
        int a = x - 1;
        int b = m - x;
        if (a > b)swap(a, b);

        if (r[a] == 'B')r[a] = 'A';
        else r[b] = 'A';
    }
    cout << r << endl;
}

