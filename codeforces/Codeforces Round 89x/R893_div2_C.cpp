void solve() {
  int n;
  cin >> n;
  vi used(n + 1);
  cout << 1 << " ";
  for (int i = 2; i <= n; i++) {
    if (used[i])
      continue;
    for (int x = i; x <= n; x *= 2) {
      cout << x << " ";
      used[x] = 1;
    }
  }
  cout << endl;
}

