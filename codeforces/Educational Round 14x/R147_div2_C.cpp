void solve() {
    string s;
    cin >> s;
    int r = 1 << 30;
    for (char x = 'a'; x <= 'z'; x++) {
        int t = 0, rr = 0;
        for (char y: s) {
            if (x == y)t = 0;
            else t++;
            rr = max(rr, t);
        }
        t = 0;
        while(rr)rr/=2, t++;
        r = min(r, t);
    }
    cout << r << endl;
}

