void solve() {
    int n, k;
    cin >> n >> k;
    vi v(n);
    for (int &x: v)cin >> x;
    int pos = -1;
    for (int i = 0; i < k; i++) {
        pos = -1;
        for (int j = 0; j < n - 1; j++) {
            if (v[j] < v[j + 1]) {
                pos = j + 1;
                v[j] ++;
                break;
            }
        }
        if (pos == -1) break;

    }
    cout << pos << endl;
}

