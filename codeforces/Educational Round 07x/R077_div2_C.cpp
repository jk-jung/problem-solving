void solve() {
    int a, b, k;
    cin >> a >> b >> k;
    if (a > b)swap(a, b);
    int x = gcd(a, b);
    int cnt = (b - x) / a + 1;
    if ((b - x) % a == 0)cnt--;
    cout << (cnt >= k ? "REBEL" : "OBEY") << endl;
}

