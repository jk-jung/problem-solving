#define MOD 998244353

class Modular {
public:
    ll mod;
    vector<ll> fac, facInv;

    Modular(int n, ll _mod) {
        mod = _mod;
        fac.resize(n + 1), facInv.resize(n + 1);

        fac[0] = 1;
        for (int i = 1; i <= n; i++) fac[i] = fac[i - 1] * i % mod;

        facInv[n] = inv(fac[n]);
        for (int i = n; i > 0; i--) facInv[i - 1] = facInv[i] * i % mod;
    }

    ll pow(ll x, ll y) {
        ll r = 1;
        for (x = x % mod; y; y >>= 1, x = (x * x) % mod)
            if (y & 1) r = (r * x) % mod;
        return r;
    }

    ll inv(ll x) { return pow(x, mod - 2); }

    ll nCk(ll n, ll k) {
        if (n < k || n < 0 || k < 0) return 0;
        if (k == 0) return 1;
        return fac[n] * facInv[k] % mod * facInv[n - k] % mod;
    }

    ll nHk(ll n, ll k) {
        return nCk(n + k - 1, k);
    }
} md(255, MOD);


ll d[255][255];

void solve() {
    int n, k;
    cin >> n >> k;

    d[0][0] = 1;
    for (int i = 1; i <= k; i++) {
        for (int j = 0; j < n; j++) {
            for (int x = 1; j + x < n; x++) {
                // weight, count
                int m = n - 1 - j;
                ll cnt = md.nCk(x, 2) + j * x;
                ll t = d[i - 1][j] * md.nCk(m, x) % MOD * md.pow(k - i + 1, cnt) % MOD;
                d[i][j + x] = (d[i][j + x] + t) % MOD;
            }
        }
        for (int j = 0; j < n; j++) {
            d[i][j] = (d[i][j] + d[i - 1][j]) % MOD;
        }
    }

    ll r = d[k][n-1];
    cout << r << endl;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    solve();
}
