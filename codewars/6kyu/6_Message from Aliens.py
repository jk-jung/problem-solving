
def decode(m):
    t = {
        '|-|':'h',
        '[-':'e',
        '()':'o',
        '3]':'b',
        '_|':'l',
        '|':'i',
        '^|':'p',
        '/`':'y',
        ')(':'o',
        '?/':'r',
        '\/':'v',
        '|\|':'n',
        '</':'k',
        '~|~':'t',
        '=/':'f',
        ')|':'d',
        '|_|':'u',
        '(':'c',
        '-[':'e',
        '~\_':'s',
        '-[':'e',
        ']3':'b',
        '_/~':'z',
        '/\\/\\':'w',
        '<>':'x',
        '/\\':'a',
        '|/\|':'m',
        '_)(':'q',
        'T_':'j',
        ',_(':'g',
        '__':' '
    }
    m = [x for x in m.split(m[0]) if x]
    return ''.join(t.get(x, t[x[::-1]]) for x in m)[::-1]
