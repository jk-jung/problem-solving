t = []
t.append("  @@@@    **    ****   ****  **  ** ******    @@  @@@@@@  @@@@   @@@@ ")
t.append(" @@  @@  ***   **  ** **  ** **  ** **       @@   @@  @@ @@  @@ @@  @@")
t.append(" @@  @@ * **      **     **  **  ** *****   @@@@     @@   @@@@  @@  @@")
t.append(" @@  @@   **     **      **   *****     ** @@  @@   @@    @@@@   @@@@ ")
t.append(" @@  @@   **    **    **  **     **     ** @@  @@  @@    @@  @@   @@  ")
t.append("  @@@@  ****** ******  ****      ** *****   @@@@   @@     @@@@   @@   ")

def print_number(n, c):
    r = []
    for s in t:
        a = ''
        for x in map(int, '%05d' % n):
            a += s[x * 7:x * 7 + 7]
        r.append(a)
    r = [c + ' ' + x + '  ' + c for x in r]
    return '\n'.join([c * 40, c + ' ' * 38 + c, *r,  c + ' ' * 38 + c, c * 40]).replace('*', c).replace('@', c)
